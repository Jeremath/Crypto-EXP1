import base64

def English_Scoring(t):
    letter_frequency = {
        'a': .08167, 'b': .01492, 'c': .02782, 'd': .04253,
        'e': .12702, 'f': .02228, 'g': .02015, 'h': .06094,
        'i': .06094, 'j': .00153, 'k': .00772, 'l': .04025,
        'm': .02406, 'n': .06749, 'o': .07507, 'p': .01929,
        'q': .00095, 'r': .05987, 's': .06327, 't': .09056,
        'u': .02758, 'v': .00978, 'w': .02360, 'x': .00150,
        'y': .01974, 'z': .00074, ' ': .15000
    }
    return sum([letter_frequency.get(chr(i), 0) for i in t.lower()])  # 总字母频率
def Single_XOR(s, single_character):
    return bytes([i ^ single_character for i in s])

def Repeating_key_XOR(_cipher, _key):
    return bytes([_cipher[i] ^ _key[i % len(_key)] for i in range(len(_cipher))])

def hamming_distance(a, b):
    return sum(bin(i ^ j).count('1') for i, j in zip(a, b))

def ciphertext_XOR(s):
    _data = [
        {
            'Single character': single_character,
            'ciphertext': Single_XOR(s, single_character),
            'score': English_Scoring(Single_XOR(s, single_character))
        }
        for single_character in range(256)
    ]
    
    best_score = max(_data, key=lambda x: x['score'])  # 找出分数最高的
    return best_score

def Get_the_keysize(ciphertext):
    data = [
        {
            'keysize': keysize,
            'distance': sum(
                hamming_distance(ciphertext[i:i + keysize], ciphertext[i + keysize:i + 2 * keysize]) / keysize
                for i in range(0, len(ciphertext) - 2 * keysize, keysize)
            ) / (len(ciphertext) // keysize - 1)
        }
        for keysize in range(2, 41)
    ]
    return min(data, key=lambda x: x['distance'])

def Break_repeating_key_XOR(ciphertext):
    _keysize = Get_the_keysize(ciphertext)  # 猜测得到的密钥长度
    keysize = _keysize['keysize']
    
    key = b''
    blocks = [ciphertext[i:i + keysize] for i in range(0, len(ciphertext), keysize)]
    
    # 对每个密钥字符位置进行破解
    for i in range(keysize):
        transposed_block = bytes(block[i] for block in blocks if len(block) > i)  # 同一列位置字符组合
        score = ciphertext_XOR(transposed_block)  # 使用频率分析破解出单字符密钥
        key += bytes([score['Single character']])  # 构造密钥
    
    # 用破解出的密钥解密整个密文
    message = Repeating_key_XOR(ciphertext, key)
    return message, key

